package String;

public class StringUsingIfElse {

	public static void main(String[] args) 
	{
		int n=24;
		if(n%5==0)
		{
			System.out.println("hi");
		}
		else if(n%3==0)
		{
			System.out.println();
		}
		else if(n%5==0 && n%3==0)
		{
			System.out.println("hi bye");
		}
		else
		{
			System.out.println("good bye");
		}
		
		

	}

}
